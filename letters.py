def generate_art(text):
    text = text.upper()
    lines = [""] * 7
    
    for char in text:
        if char in LETTERS:
            char_lines = LETTERS[char]
            for i in range(7):
                lines[i] += char_lines[i] + "  "  # Add spacing
        else:
            # Placeholder for unknown chars
            for i in range(7):
                lines[i] += "       " + "  "
                
    return "\n".join(lines)
